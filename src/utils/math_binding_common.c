MTR_SCRIPT_FUNC(MTR_SF_Vecxy_f)
{
    float length;
    float dir;
    float x;
    float y;

    MTR_SF_GET_SINGLE(length, 1);
    MTR_SF_GET_SINGLE(dir, 2);
    MTR_Vecxy_f(length, dir, &x, &y);

    MTR_SF_PUSH_SINGLE(x);
    MTR_SF_PUSH_SINGLE(y);

    return 2;
}

